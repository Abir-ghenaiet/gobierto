          </div>
        </template>
        <template v-else>
          {{ attributes[trId] }}
        </template>
      </td>
    </template>
  </tr>
</template>

<script>
export default {
  name: "ProjectsByTermTableRow",
  props: {
    marked: {
      type: Boolean,
      default: false
    },
    projectId: {
      type: String,
      default: ''
    },
    columns: {
      type: Array,
      default: () => []
    },
    attributes: {
      type: Object,
      default: () => {}
    },
  },
  methods: {
