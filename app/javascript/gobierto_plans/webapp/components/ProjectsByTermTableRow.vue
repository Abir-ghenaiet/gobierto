<template>
  <tr>
    <template v-for="[trId, [, , trVisibility]] in columns">
      <td
        v-if="trVisibility"
        :key="trId"
        :style="{ fontWeight: marked ? 900 : 400 }"
      >
        <!-- TODO: faltan definir ciertos tipos de plantilla segÃºn su type -->
        <template v-if="trId === 'name'">
